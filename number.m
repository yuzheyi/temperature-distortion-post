clear all
% 空气参数环境压力，环境温度，空气中氧气的质量分数
P=1.01*10^5,T=300,fraction_o=0.4
%爆震管参数
l=2,d=0.08,f=20
% 燃油参数燃油流量，燃油与氧气反应的恰当比（一公斤燃油要消耗多少氧气）
m_f=0.5466,phi=3.429


%%% 计算密度
%% M=28,R=314,P=1.01*10^5,T=300
M_o=32,M_n=28,R=8314
R_g_o=R/M_o
R_g_n=R/M_n
rho=fraction_o*P/(T*R_g_o)+(1-fraction_o)*P/(T*R_g_n)


% 计算达到温度所需的总燃气流量
%计算所需要的氧气
m_o=phi*m_f
%更具所需氧气计算总质量流量
m_t=m_o/fraction_o
%更具当地工况计算体积流量
V_t=m_t/rho

% 计算单根爆震燃烧室总流量
V_det=f*l*(d/2)^2*pi

% 计算数量
n=V_t/V_det

